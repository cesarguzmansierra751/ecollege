--Consulta de datos
SELECT 
    c.nombre AS NombreCurso,
    COUNT(i.id_estudiante) AS CantidadEstudiantes
FROM 
    Cursos c
JOIN 
    Inscripciones i ON c.id_curso = i.id_curso
GROUP BY 
    c.nombre
HAVING 
    COUNT(i.id_estudiante) > 1
ORDER BY 
    CantidadEstudiantes DESC;


SELECT 
    e.nombre AS NombreEstudiante,
    e.apellido AS ApellidoEstudiante,
    c.nombre AS NombreCurso,
    p.nombre AS NombreProfesor,
    p.apellido AS ApellidoProfesor,
    i.fecha_inscripcion AS FechaInscripcion
FROM     Inscripciones i JOIN      Estudiantes e ON i.id_estudiante = e.id_estudiante
JOIN     Cursos c ON i.id_curso = c.id_curso JOIN  Profesores p ON c.id_profesor = p.id_profesor
ORDER BY c.nombre, e.apellido;
